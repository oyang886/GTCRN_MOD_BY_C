void calc_conv1(float(*input)[INPUT_FREQ], float(* output)[OUTPUT_FREQ])
{
	int cout, f_out, sfe_cin;
	int counter = -2;
	float bias;
	float bn_w;
	float bn_b;
	float bn_m;
	float inv_std;
	float sum;
	float w0, w1, w2, w3, w4;
	float x0, x1, x2, x3, x4;
	float bn_result;
    int base;

	// 输出为16x65
	for (cout = 0; cout < OUT_CHANNELS; cout++)
	{
		bias = conv_bias[cout];
		bn_w = bn_weight[cout];
		bn_b = bn_bias[cout];
		bn_m = bn_mean[cout];
		// float bn_v = bn_var[cout];
		// float inv_std = 1.0f / sqrtf(bn_v + BN_EPS);
		inv_std = calc_std[cout];

		for (f_out = 0; f_out < OUTPUT_FREQ; f_out++)
		{
			sum = bias;

			for (sfe_cin = 0; sfe_cin < SFE_CHANNELS; sfe_cin++)
			{
				float* weight_row = &conv_weights[cout][sfe_cin][0][0];
                base = counter;

				w0 = weight_row[0];
				w1 = weight_row[1];
				w2 = weight_row[2];
				w3 = weight_row[3];
				w4 = weight_row[4];
                /*
				if (counter < 0)
				{
					x0 = 0.0f;
					x1 = 0.0f;
					x2 = input[sfe_cin][counter + 2];
					x3 = input[sfe_cin][counter + 3];
					x4 = input[sfe_cin][counter + 4];
				}
				else if ((counter + 2)>= 128)
				{
					x0 = input[sfe_cin][counter];
					x1 = input[sfe_cin][counter + 1];
					x2 = input[sfe_cin][counter + 2];
					x3 = 0.0f;
					x4 = 0.0f;
				}
				else
				{
					x0 = input[sfe_cin][counter];
					x1 = input[sfe_cin][counter + 1];
					x2 = input[sfe_cin][counter + 2];
					x3 = input[sfe_cin][counter + 3];
					x4 = input[sfe_cin][counter + 4];
				}
                */

                x0 = (base + 0 >= 0 && base + 0 < INPUT_FREQ) ? input[sfe_cin][base + 0] : 0.0f;
                x1 = (base + 1 >= 0 && base + 1 < INPUT_FREQ) ? input[sfe_cin][base + 1] : 0.0f;
                x2 = (base + 2 >= 0 && base + 2 < INPUT_FREQ) ? input[sfe_cin][base + 2] : 0.0f;
                x3 = (base + 3 >= 0 && base + 3 < INPUT_FREQ) ? input[sfe_cin][base + 3] : 0.0f;
                x4 = (base + 4 >= 0 && base + 4 < INPUT_FREQ) ? input[sfe_cin][base + 4] : 0.0f;

				sum += x0 * w0 + x1 * w1 + x2 * w2 + x3 * w3 + x4 * w4;
			}

			// batch norm + prelu
			bn_result = (sum - bn_m) * inv_std * bn_w + bn_b;

			if (bn_result >= 0.0f) 
			{
				output[cout][f_out] = bn_result;
			}
			else 
			{
				output[cout][f_out] = bn_result * prelu_alpha;
			}
			counter += STRIDE;
		}
		counter = -2;
	}
}

